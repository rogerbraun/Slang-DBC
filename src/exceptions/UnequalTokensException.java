package exceptions;


public class UnequalTokensException extends Exception {

   public UnequalTokensException() {
      super();
   }

   public UnequalTokensException(String arg0) {
      super(arg0);
   }

   public UnequalTokensException(String arg0, Throwable arg1) {
      super(arg0, arg1);
   }

   public UnequalTokensException(Throwable arg0) {
      super(arg0);
   }

}
